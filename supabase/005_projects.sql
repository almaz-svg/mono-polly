-- Adds the fixed pool of startup case studies ("Проекты для распределения")
-- that each team develops during the game, and a project_id column on
-- teams so a random, no-repeat assignment can be made when the game starts.
--
-- Run this once in the Supabase SQL Editor. Safe to re-run.

create table if not exists projects (
  id uuid primary key default gen_random_uuid(),
  key text unique not null,
  title text not null,
  tagline text not null,       -- 1-2 предложения, показываются команде сразу после распределения
  what_is text not null,       -- суть кейса: сфера, ЦА, монетизация
  key_feature text not null,   -- ключевая фишка оригинального продукта
  tasks text not null,         -- что участнику нужно развить, по одному пункту на строку
  sort_order int not null default 0
);

alter table projects enable row level security;

drop policy if exists "projects_select" on projects;
create policy "projects_select" on projects for select using (true);

-- Проекты — фиксированный справочник, правится только вручную в Supabase.
revoke insert, update, delete on projects from anon, authenticated;

